package vo;

public class Ac {
                 
	private String id;
	private String acName;
	private String acDesc;
	private String acCon;
	private String acTime;
	private String acPubMan;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAcName() {
		return acName;
	}
	public void setAcName(String acName) {
		this.acName = acName;
	}
	public String getAcDesc() {
		return acDesc;
	}
	public void setAcDesc(String acDesc) {
		this.acDesc = acDesc;
	}
	public String getAcCon() {
		return acCon;
	}
	public void setAcCon(String acCon) {
		this.acCon = acCon;
	}
	public String getAcTime() {
		return acTime;
	}
	public void setAcTime(String acTime) {
		this.acTime = acTime;
	}
	public String getAcPubMan() {
		return acPubMan;
	}
	public void setAcPubMan(String acPubMan) {
		this.acPubMan = acPubMan;
	}
	
	
}
